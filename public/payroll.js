    // –û—á–∏—â–∞–µ–º —Ç–∞–±–ª–∏—Ü—É –æ—Ç –∏–Ω–ø—É—Ç–æ–≤, –∑–∞–º–µ–Ω—è—è –∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è–º–∏
