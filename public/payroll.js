    if (!table || table.rows.length === 0) {
        showStatus(statusId, '–ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –¥–ª—è —ç–∫—Å–ø–æ—Ä—Ç–∞', 'error');
        return;
    }
